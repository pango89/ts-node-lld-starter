/**
 * An enumeration of the four cardinal directions.
 */
export enum Direction {
    /**
     * The direction pointing towards the North Pole.
     */
    North,
    /**
     * The direction pointing towards the South Pole.
     */
    South,
    /**
     * The direction pointing towards the rising sun.
     */
    East,
    /**
     * The direction pointing towards the setting sun.
     */
    West
}
